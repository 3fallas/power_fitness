private void actualizarU() {
    try {
        int fila = Usuario.getSelectedRow();

        if (fila >= 0 && fila < uTabla.getRowCount()) {
            int id = Integer.parseInt(jTextField2.getText());
            String nombre = jTextField3.getText();
            String primerApellido = jTextField4.getText();
            String segundoApellido = jTextField5.getText();
            LocalDate fechaNacimiento = LocalDate.parse(jTextField6.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
            String correo = jTextField7.getText();
            String contraseña = jTextField8.getText();
            int celular = Integer.parseInt(jTextField9.getText());
            String direccion = jTextField10.getText();
            int idRol = Integer.parseInt(jTextField11.getText());
            int idMembresia = Integer.parseInt(jTextField12.getText());
            u.editarUsuario(id, nombre, primerApellido, segundoApellido, fechaNacimiento, correo, contraseña, celular, direccion, idRol, idMembresia);
            uTabla.setValueAt(id,fila,0);
            uTabla.setValueAt(nombre, fila, 1);
            uTabla.setValueAt(primerApellido, fila, 2);
            uTabla.setValueAt(segundoApellido,fila, 3);
            uTabla.setValueAt(fechaNacimiento.format(DateTimeFormatter.ofPattern("yyyy-MM-dd")), fila, 4);
            uTabla.setValueAt(correo, fila, 5);
            uTabla.setValueAt(contraseña, fila, 6);
            uTabla.setValueAt(celular,fila, 7);
            uTabla.setValueAt(direccion, fila,8);
            uTabla.setValueAt(idRol,fila,9);
            uTabla.setValueAt(idMembresia,fila,10);
            
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila de la tabla.");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al actualizar una fila: " + e.getMessage());
    }
}
public void eliminar() throws SQLException{
   int fila = Usuario.getSelectedRow();
   if (fila >= 0 && fila < uTabla.getRowCount()) {
       int UsuarioId = (int) uTabla.getValueAt(fila, 0);
      u.eliminarUsuario(UsuarioId, uTabla);
       uTabla.removeRow(fila);
       JOptionPane.showMessageDialog(null, "El producto ha sido eliminado correctamente.");
   } else {
       JOptionPane.showMessageDialog(null, "Debe seleccionar un producto de la tabla.");
   }
}
